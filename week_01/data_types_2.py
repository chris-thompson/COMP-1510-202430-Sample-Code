"""
We can use the type function to find out what data type a variable is bound to.
"""


print(type(4))
print(type(0))
print(type(-11))
print(type(0.0))
print(type(5.6))
print(type(-3.14))
print(type(True))
print(type(False))
print(type('Glycerine'))
print(type("Helen Alice"))
print(type(" "))
print(type([1, 2, 3, 4]))
print(type({True: 100, False: -100}))
print(type((0, 0)))
print(type({1, 2, 3}))
print(type(None))
